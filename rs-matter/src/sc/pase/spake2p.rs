#[allow(non_snake_case)]
pub struct CryptoSpake2<'a, C: Crypto> {
    crypto: &'a C,
    M: C::Secp256r1Point<'a>,
    N: C::Secp256r1Point<'a>,
    xy: Option<C::Secp256r1Scalar<'a>>,
    w0: Option<C::Secp256r1Scalar<'a>>,
    w1: Option<C::Secp256r1Scalar<'a>>,
    L: Option<C::Secp256r1Point<'a>>,
    pB: Option<C::Secp256r1Point<'a>>,
}

impl<'a, C: Crypto> CryptoSpake2<'a, C> {
    #[allow(non_snake_case)]
    pub fn new(crypto: &'a C) -> Result<Self, Error> {
        Ok(Self {
            crypto,
            M: crypto.secp256r1_point(MATTER_M_BIN)?,
            N: crypto.secp256r1_point(MATTER_N_BIN)?,
            xy: None,
            w0: None,
            w1: None,
            L: None,
            pB: None,
        })
    }

    // Computes w0 from w0s respectively
    pub fn set_w0_from_w0s(&mut self, w0s: &CanonSecp256r1Scalar) -> Result<(), Error> {
        // From the Matter Spec,
        //         w0 = w0s mod p
        //   where p is the order of the curve
        const OPERAND: &CanonSecp256r1Scalar = &[
            0xff, 0xff, 0xff, 0xff, 0x00, 0x00, 0x00, 0x00, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff,
            0xff, 0xff, 0xbc, 0xe6, 0xfa, 0xad, 0xa7, 0x17, 0x9e, 0x84, 0xf3, 0xb9, 0xca, 0xc2,
            0xfc, 0x63, 0x25, 0x51,
        ];

        self.w0 = Some(self.rem(w0s, OPERAND)?);

        Ok(())
    }

    pub fn set_w1_from_w1s(&mut self, w1s: &CanonSecp256r1Scalar) -> Result<(), Error> {
        // From the Matter Spec,
        //         w1 = w1s mod p
        //   where p is the order of the curve
        const OPERAND: &CanonSecp256r1Scalar = &[
            0xff, 0xff, 0xff, 0xff, 0x00, 0x00, 0x00, 0x00, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff,
            0xff, 0xff, 0xbc, 0xe6, 0xfa, 0xad, 0xa7, 0x17, 0x9e, 0x84, 0xf3, 0xb9, 0xca, 0xc2,
            0xfc, 0x63, 0x25, 0x51,
        ];

        self.w1 = Some(self.rem(w1s, OPERAND)?);

        Ok(())
    }

    fn rem(
        &self,
        value: &CanonSecp256r1Scalar,
        operand: &CanonSecp256r1Scalar,
    ) -> Result<C::Secp256r1Scalar<'a>, Error> {
        let value = self.expand(value)?;
        let operand = self.expand(operand)?;

        let result = value.rem(&operand).unwrap();
        let mut result_uint = UINT384_ZEROED;
        result.canon_into(&mut result_uint);

        let result_scalar: &CanonSecp256r1Scalar = unwrap!(as_canon(&result_uint[16..]));

        self.crypto.secp256r1_scalar(result_scalar)
    }

    fn expand(&self, scalar: &CanonSecp256r1Scalar) -> Result<C::UInt384<'a>, Error> {
        let mut operand_u384 = UINT384_ZEROED;
        let scalar_buf: &mut CanonSecp256r1Scalar = unwrap!(as_canon_mut(&mut operand_u384[16..]));
        scalar_buf.copy_from_slice(scalar);

        self.crypto.uint384(&operand_u384)
    }

    pub(crate) fn set_w0(&mut self, w0: &CanonSecp256r1Scalar) -> Result<(), Error> {
        self.w0 = Some(self.crypto.secp256r1_scalar(w0)?);
        Ok(())
    }

    pub(crate) fn set_w1(&mut self, w1: &CanonSecp256r1Scalar) -> Result<(), Error> {
        self.w1 = Some(self.crypto.secp256r1_scalar(w1)?);
        Ok(())
    }

    #[allow(non_snake_case)]
    #[allow(dead_code)]
    pub fn set_L(&mut self, l: &CanonSecp256r1Point) -> Result<(), Error> {
        self.L = Some(self.crypto.secp256r1_point(l)?);
        Ok(())
    }

    #[allow(non_snake_case)]
    pub fn set_L_from_w1s(&mut self, w1s: &CanonSecp256r1Scalar) -> Result<(), Error> {
        // From the Matter spec,
        //        L = w1 * P
        //    where P is the generator of the underlying elliptic curve
        self.set_w1_from_w1s(w1s)?;
        self.L = Some(
            self.crypto
                .secp256r1_generator()?
                .mul(unwrap!(self.w1.as_ref())),
        );
        Ok(())
    }

    #[allow(non_snake_case)]
    pub fn get_pB(&mut self, pB: &mut CanonSecp256r1Point) -> Result<(), Error> {
        // From the SPAKE2+ spec (https://datatracker.ietf.org/doc/draft-bar-cfrg-spake2plus/)
        //   for y
        //   - select random y between 0 to p
        //   - Y = y*P + w0*N
        //   - pB = Y
        let xy = self.crypto.secp256r1_scalar_random()?;

        let pb =
            self.crypto
                .secp256r1_generator()?
                .add_mul(&xy, &self.N, unwrap!(self.w0.as_ref()));

        pb.canon_into(pB);

        self.xy = Some(xy);
        self.pB = Some(pb);

        Ok(())
    }

    #[allow(non_snake_case)]
    pub fn get_TT_as_verifier(
        &mut self,
        context: &[u8],
        pA: &CanonSecp256r1Point,
        pB: &CanonSecp256r1Point,
        out: &mut Sha256Hash,
    ) -> Result<(), Error> {
        let mut TT = self.crypto.sha256()?;

        // Context
        Self::add_to_tt(&mut TT, context);
        // 2 empty identifiers
        Self::add_to_tt(&mut TT, &[]);
        Self::add_to_tt(&mut TT, &[]);
        // M
        Self::add_to_tt(&mut TT, MATTER_M_BIN);
        // N
        Self::add_to_tt(&mut TT, MATTER_N_BIN);
        // X = pA
        Self::add_to_tt(&mut TT, pA);
        // Y = pB
        Self::add_to_tt(&mut TT, pB);

        let X = self.crypto.secp256r1_point(pA)?;
        let (Z, V) = ZV::new(self.crypto).verifier(
            unwrap!(self.w0.as_ref()),
            unwrap!(self.L.as_ref()),
            &self.M,
            &X,
            unwrap!(self.xy.as_ref()),
        );

        let mut point = SECP256R1_POINT_ZEROED;

        // Z
        Z.canon_into(&mut point);
        Self::add_to_tt(&mut TT, &point);
        // V
        V.canon_into(&mut point);
        Self::add_to_tt(&mut TT, &point);
        // w0
        let scalar: &mut CanonSecp256r1Scalar =
            unwrap!(as_canon_mut(&mut point[..SECP256R1_CANON_SCALAR_LEN]));
        unwrap!(self.w0.as_ref()).canon_into(scalar);
        Self::add_to_tt(&mut TT, scalar);

        TT.finish(out);

        Ok(())
    }

    fn add_to_tt(tt: &mut C::Sha256<'_>, data: &[u8]) {
        tt.update(&(data.len() as u64).to_le_bytes());
        if !data.is_empty() {
            tt.update(data);
        }
    }
}

struct ZV<'a, C: Crypto>(PhantomData<&'a C>);

impl<'a, C: Crypto> ZV<'a, C> {
    pub const fn new(_crypto: &'a C) -> Self {
        Self(PhantomData)
    }

    #[inline(always)]
    #[allow(non_snake_case)]
    #[allow(dead_code)]
    fn verifier(
        &self,
        w0: &C::Secp256r1Scalar<'a>,
        L: &C::Secp256r1Point<'a>,
        M: &C::Secp256r1Point<'a>,
        X: &C::Secp256r1Point<'a>,
        y: &C::Secp256r1Scalar<'a>,
    ) -> (C::Secp256r1Point<'a>, C::Secp256r1Point<'a>) {
        // As per the RFC, the operation here is:
        //   Z = h*y*(X - w0*M)
        //   V = h*y*L

        // We will follow the same sequence as in C++ SDK, under the assumption
        // that the same sequence works for all embedded platforms. So the step
        // of operations is:
        //    tmp = y*w0
        //    Z = y*X + tmp*M (M is inverted to get the 'negative' effect)
        //    Z = h*Z (cofactor Mul)

        let Z = X.add_mul(y, &M.neg(), &y.mul(w0));

        // Cofactor for P256 is 1, so that is a No-Op

        let V = L.mul(y);

        (Z, V)
    }

    #[inline(always)]
    #[allow(non_snake_case)]
    #[allow(dead_code)]
    fn prover(
        &self,
        w0: &C::Secp256r1Scalar<'a>,
        w1: &C::Secp256r1Scalar<'a>,
        N: &C::Secp256r1Point<'a>,
        Y: &C::Secp256r1Point<'a>,
        x: &C::Secp256r1Scalar<'a>,
    ) -> (C::Secp256r1Point<'a>, C::Secp256r1Point<'a>) {
        // As per the RFC, the operation here is:
        //   Z = h*x*(Y - w0*N)
        //   V = h*w1*(Y - w0*N)

        // We will follow the same sequence as in C++ SDK, under the assumption
        // that the same sequence works for all embedded platforms. So the step
        // of operations is:
        //    tmp = x*w0
        //    Z = x*Y + tmp*N (N is inverted to get the 'negative' effect)
        //    Z = h*Z (cofactor Mul)

        let N_neg = N.neg();

        let Z = Y.add_mul(x, &N_neg, &x.mul(w0));

        // Cofactor for P256 is 1, so that is a No-Op

        let V = Y.add_mul(w1, &N_neg, &w1.mul(w0));

        (Z, V)
    }
}

pub const SPAKE2_ITERATION_COUNT: u32 = 2000;
pub const MAX_SALT_SIZE_BYTES: usize = 32;

const SPAKE2P_KEY_CONFIRM_INFO: &[u8] = b"ConfirmationKeys";
const SPAKE2P_CONTEXT_PREFIX: &[u8] = b"CHIP PAKE V1 Commissioning";

const VERIFIER_SIZE_BYTES: usize = SECP256R1_CANON_SCALAR_LEN + SECP256R1_CANON_POINT_LEN;

#[derive(PartialEq, Copy, Clone, Debug)]
#[cfg_attr(feature = "defmt", derive(defmt::Format))]
pub enum Spake2VerifierState {
    // Initialised - w0, L are set
    Init,
    // Pending Confirmation - Keys are derived but pending confirmation
    PendingConfirmation,
    // Confirmed
    Confirmed,
}

#[derive(PartialEq, Debug)]
#[cfg_attr(feature = "defmt", derive(defmt::Format))]
pub enum Spake2Mode {
    Unknown,
    #[allow(unused)]
    Prover,
    Verifier(Spake2VerifierState),
}

#[allow(non_snake_case)]
pub struct Spake2P<'a, C: Crypto> {
    mode: Spake2Mode,
    pub(crate) crypto: &'a C,
    context: Option<C::Sha256<'a>>,
    spake2: Option<CryptoSpake2<'a, C>>,
    Ke: [u8; 16],
    cA: HmacSha256Hash,
    app_data: u32,
}

impl<'a, C: Crypto> Spake2P<'a, C> {
    pub const fn new(crypto: &'a C) -> Self {
        Self {
            mode: Spake2Mode::Unknown,
            crypto,
            context: None,
            spake2: None,
            Ke: [0; 16],
            cA: [0; 32],
            app_data: 0,
        }
    }

    #[allow(non_snake_case)]
    pub fn init(crypto: &'a C) -> impl Init<Self> {
        init!(Self {
            mode: Spake2Mode::Unknown,
            crypto,
            context: None,
            spake2: None,
            Ke <- init_zeroed(),
            cA <- init_zeroed(),
            app_data: 0,
        })
    }

    pub fn set_app_data(&mut self, data: u32) {
        self.app_data = data;
    }

    pub fn get_app_data(&mut self) -> u32 {
        self.app_data
    }

    pub fn set_context(&mut self) -> Result<(), Error> {
        let mut context = self.crypto.sha256()?;
        context.update(SPAKE2P_CONTEXT_PREFIX);
        self.context = Some(context);
        Ok(())
    }

    pub fn update_context(&mut self, buf: &[u8]) -> Result<(), Error> {
        unwrap!(self.context.as_mut()).update(buf);

        Ok(())
    }

    pub fn ke(&self) -> &[u8; 16] {
        &self.Ke
    }

    fn get_w0w1s(&self, pw: u32, iter: u32, salt: &[u8], w0w1s: &mut [u8; UINT384_CANON_LEN * 2]) {
        unwrap!(self.crypto.pbkdf2_hmac_sha256()).derive(
            &pw.to_le_bytes(),
            iter as usize,
            salt,
            w0w1s,
        );
    }

    pub(crate) fn start_verifier(&mut self, verifier: &VerifierData) -> Result<(), Error> {
        self.spake2 = Some(CryptoSpake2::new(self.crypto)?);
        if let Some(pw) = verifier.password {
            // Derive w0 and L from the password
            let mut w0w1s = [0; 2 * UINT384_CANON_LEN];
            self.get_w0w1s(pw, verifier.count, &verifier.salt, &mut w0w1s);

            if let Some(spake2) = &mut self.spake2 {
                spake2.set_w0_from_w0s(unwrap!(as_canon(&w0w1s[..UINT384_CANON_LEN])))?;
                spake2.set_L_from_w1s(unwrap!(as_canon(&w0w1s[UINT384_CANON_LEN..])))?;
            }
        } else {
            // Extract w0 and L from the verifier
            if let Some(spake2) = &mut self.spake2 {
                spake2.set_w0(unwrap!(as_canon(
                    &verifier.verifier[..SECP256R1_CANON_SCALAR_LEN]
                )))?;
                spake2.set_L(unwrap!(as_canon(
                    &verifier.verifier[SECP256R1_CANON_SCALAR_LEN..]
                )))?;
            }
        }

        self.mode = Spake2Mode::Verifier(Spake2VerifierState::Init);
        Ok(())
    }

    #[allow(non_snake_case)]
    pub fn handle_pA(
        &mut self,
        pA: &CanonSecp256r1Point,
        pB: &mut CanonSecp256r1Point,
        cB: &mut HmacSha256Hash,
    ) -> Result<(), Error> {
        if self.mode != Spake2Mode::Verifier(Spake2VerifierState::Init) {
            Err(ErrorCode::InvalidState)?;
        }

        if let Some(crypto_spake2) = &mut self.spake2 {
            crypto_spake2.get_pB(pB)?;
            if let Some(context) = self.context.take() {
                let mut hash = SHA256_HASH_ZEROED;
                context.finish(&mut hash);
                let mut TT = SHA256_HASH_ZEROED;
                crypto_spake2.get_TT_as_verifier(&hash, pA, pB, &mut TT)?;

                Self::get_Ke_and_cAcB(self.crypto, &TT, pA, pB, &mut self.Ke, &mut self.cA, cB)?;
            }
        }

        // We are finished with using the crypto_spake2 now
        self.spake2 = None;
        self.mode = Spake2Mode::Verifier(Spake2VerifierState::PendingConfirmation);
        Ok(())
    }

    #[allow(non_snake_case)]
    pub fn handle_cA(&mut self, cA: &[u8]) -> Result<(), SCStatusCodes> {
        if self.mode != Spake2Mode::Verifier(Spake2VerifierState::PendingConfirmation) {
            return Err(SCStatusCodes::SessionNotFound);
        }
        self.mode = Spake2Mode::Verifier(Spake2VerifierState::Confirmed);
        if cA.ct_eq(&self.cA).unwrap_u8() == 1 {
            Ok(())
        } else {
            Err(SCStatusCodes::InvalidParameter)
        }
    }

    #[allow(non_snake_case)]
    #[allow(dead_code)]
    fn get_Ke_and_cAcB(
        crypto: &C,
        TT: &Sha256Hash,
        pA: &[u8],
        pB: &[u8],
        Ke: &mut [u8; 16],
        cA: &mut HmacSha256Hash,
        cB: &mut HmacSha256Hash,
    ) -> Result<(), Error> {
        // Step 1: Ka || Ke = Hash(TT)
        let KaKe = TT;
        let Ka = &KaKe[..SHA256_HASH_LEN / 2];
        let ke_internal = &KaKe[SHA256_HASH_LEN / 2..];
        Ke.copy_from_slice(ke_internal);

        // Step 2: KcA || KcB = KDF(nil, Ka, "ConfirmationKeys")
        let mut KcAKcB: [u8; 32] = [0; 32];
        crypto
            .hkdf_sha256()
            .unwrap()
            .expand(&[], Ka, SPAKE2P_KEY_CONFIRM_INFO, &mut KcAKcB)
            .map_err(|_x| ErrorCode::InvalidData)?;

        let KcA = &KcAKcB[..KcAKcB.len() / 2];
        let KcB = &KcAKcB[KcAKcB.len() / 2..];

        // Step 3: cA = HMAC(KcA, pB), cB = HMAC(KcB, pA)
        let mut mac = crypto.hmac_sha256(KcA)?;
        mac.update(pB);
        mac.finish(cA);

        let mut mac = crypto.hmac_sha256(KcB)?;
        mac.update(pA);
        mac.finish(cB);
        Ok(())
    }
}

#[derive(Debug, Clone)]
#[cfg_attr(feature = "defmt", derive(defmt::Format))]
pub(crate) struct VerifierData {
    /// When `password` is `None`, `verifier` is expected to be set
    pub password: Option<u32>,
    pub verifier: [u8; VERIFIER_SIZE_BYTES],
    // For the VerifierOption::Verifier, the following fields only serve
    // information purposes
    pub salt: [u8; MAX_SALT_SIZE_BYTES],
    pub count: u32,
}

impl VerifierData {
    pub fn init_with_pw(password: u32, rand: Rand) -> impl Init<Self> {
        Self::init_empty().chain(move |this| {
            this.configure_pw(password, rand);

            Ok(())
        })
    }

    pub fn init<'a>(verifier: &'a [u8], salt: &'a [u8], count: u32) -> impl Init<Self, Error> + 'a {
        Self::init_empty()
            .into_fallible()
            .chain(move |this| this.configure_verifier(verifier, salt, count))
    }

    fn init_empty() -> impl Init<Self> {
        init!(Self {
            password: None,
            verifier <- init_zeroed(),
            salt <- init_zeroed(),
            count: SPAKE2_ITERATION_COUNT,
        })
    }

    fn configure_pw(&mut self, password: u32, rand: Rand) {
        self.password = Some(password);
        rand(&mut self.salt);
    }

    fn configure_verifier(
        &mut self,
        verifier: &[u8],
        salt: &[u8],
        count: u32,
    ) -> Result<(), Error> {
        if salt.len() != self.salt.len() || verifier.len() != self.verifier.len() {
            Err(ErrorCode::InvalidData)?;
        }

        self.password = None;
        self.salt.copy_from_slice(salt);
        self.verifier.copy_from_slice(verifier);
        self.count = count;

        Ok(())
    }
}

